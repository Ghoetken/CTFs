#define MAX 12
intptr_t *deals[MAX];

void menu() {
    puts("=== Black Market Broker ===");
    puts("1. Open Deal");
    puts("2. Cancel Deal");
    puts("3. Edit Deal");
    puts("4. View Deal");
    puts("5. Close Broker");
    printf("> ");
}

void open_deal() {
    int idx;
    size_t size;
    printf("Deal index: ");
    scanf("%d", &idx);
    if (idx < 0 || idx >= MAX) {
        puts("Invalid deal index!");
        return;
    }
    printf("Deal size: ");
    scanf("%lu", &size);
    deals[idx] = malloc(size);
    printf("Deal opened at %p\n", deals[idx]);
}

void cancel_deal() {
    int idx;
    printf("Deal index: ");
    scanf("%d", &idx);
    if (idx < 0 || idx >= MAX || !deals[idx]) {
        puts("Invalid deal index!");
        return;
    }
    free(deals[idx]);
}

void edit_deal() {
    int idx;
    printf("Deal index: ");
    scanf("%d", &idx);
    if (idx < 0 || idx >= MAX || !deals[idx]) {
        puts("Invalid deal index!");
        return;
    }
    printf("Edit deal data: ");
    getchar(); // clear newline
    read(0, deals[idx], 0x120);
}

void view_deal() {
    int idx;
    printf("Deal index: ");
    scanf("%d", &idx);
    if (idx < 0 || idx >= MAX || !deals[idx]) {
        puts("Invalid deal index!");
        return;
    }
    printf("Deal is at %p\n", deals[idx]);
}

